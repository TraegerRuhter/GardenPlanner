/**
 * Stage sprite pixel maps (§13.5, §21.4). Multiple shape families so
 * different plant types get distinct silhouettes. Characters are palette
 * slots — a single map renders per-category (and per-plant accent) variants:
 *
 *   .  transparent      s  stem          f  accent (fruit/bloom)
 *   m  soil light       l  leaf          F  accent shade
 *   M  soil dark        L  leaf shade    y  dry/senescent
 *   w  wood/stub
 */

